// Soma + | Subtração - | Multiplicação * | Divisão / | Potenciação ** | Resto %

const num1 = 10
const num2 = 5
const soma = num1 + num2
const multiplicacao = num1 * num2
const divisao = num1 / num2
const potenciacao = num1 ** 2
const resto = num1 % 3

console.log("Resultado da soma:", soma)
console.log("Resultado da multiplicação:", multiplicacao)
console.log("Resultado da divisão:", divisao)
console.log("Resultado da potenciação:", potenciacao)
console.log("Resultado do resto da divisão:", resto)